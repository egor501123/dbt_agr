    a.liability_rur,
    a.liability_rur_rkk,
    -- Для определения не карт, среди открытых/закрытых, по дате открытия
    a.start_desc_num,
    a.start_asc_num,
    -- Для определения не карт, среди открытых/закрытых, по дате закрытия
    a.end_desc_num,
